num1 = int(input("Enter a Number: "))
num2 = int(input("Enter another Number: "))
#num increment by 10

num1+=num2
print(f"New value of Num 1 {num1}")

#num decrement by 10;

num1-=num2
print(f"New value of Num 1 {num1}")

#num multiply by 10

num1*=num2
print(f"New value of Num 1 {num1}")

#num divide by 10

num1/=num2
print(f"New value of Num 1 {num1}")